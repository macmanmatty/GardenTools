package com.example.FruitTrees.ChillingHours;

public class ChillingCalculationMethod {
    private  double minChillingTemp;
    private  double maxChillingTemp;
    private String name;

    public double getMinChillingTemp() {
        return minChillingTemp;
    }

    public void setMinChillingTemp(double minChillingTemp) {
        this.minChillingTemp = minChillingTemp;
    }

    public double getMaxChillingTemp() {
        return maxChillingTemp;
    }

    public void setMaxChillingTemp(double maxChillingTemp) {
        this.maxChillingTemp = maxChillingTemp;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
